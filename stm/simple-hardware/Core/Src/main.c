int set_led_blink_frequency(uint8_t data){

	uint16_t target_frequency = (uint16_t)data;
	uint32_t timer_period = (HAL_RCC_GetHCLKFreq()/(htim2.Instance->PSC * target_frequency*2)) - 1;
	htim2.Instance->CNT = 0; // reset the value of the counter
	htim2.Instance->ARR = timer_period; // writing to the autoreload register.

	return 0;

}

int peripheral_action(Payload* payload){
	// This function takes payload as argument as delegates the proper action based on its attributes
	int status = 0;
	switch(payload->device_type){
		case 'l':
			// It is an led. So, blink the LED at the given frequency
			set_led_blink_frequency(payload->data);
			break;

		case 's':
			// It is a servo motor. So rotate the motor to the set degree
			rotate_servo(&servo_motor, payload->data);
			break;

		default:
			status = 1;
	}

	return status;

}


